  function normalizeLang(value) {
    const raw = String(value || "").trim();
    const lower = raw.toLowerCase();
    if (lower === "jp" || lower.startsWith("ja")) return "ja";
    if (lower.startsWith("ko")) return "ko";
    if (lower.startsWith("fr")) return "fr";
    if (lower.startsWith("de")) return "de";
    if (lower.startsWith("th")) return "th";
    if (lower === "in" || lower.startsWith("id")) return "id";
    if (lower === "zh" || lower === "zh-cn" || lower.includes("hans")) return "zh-CN";
    if (lower === "zh-tw" || lower === "zh-hk" || lower.includes("hant")) return "zh-TW";
    if (lower.startsWith("en")) return "en";
    return dictionaries[raw] ? raw : "ko";
  }

  function selectedLang() {
    const params = new URL(location.href).searchParams;
    const fromUrl = params.get("lang");
    if (fromUrl) return normalizeLang(fromUrl);
    return normalizeLang(localStorage.getItem("siteLang") || localStorage.getItem("lang") || document.documentElement.lang || "ko");
  }

  function patchPageDictionaries() {
    try {
      if (typeof I18N !== "undefined" && I18N) {
        for (const { code } of LANGUAGES) {
          I18N[code] = { ...(I18N.en || {}), ...(I18N[code] || {}), ...(dictionaries[code] || {}) };
        }
        I18N.zh = I18N["zh-CN"];
      }
    } catch (_) {}
  }

  function installOptions(select, lang) {
    if (!select) return;
    const current = normalizeLang(select.value || lang);
    select.classList.add("legend-lang-select");
    select.innerHTML = LANGUAGES.map((item) => `<option value="${item.code}">${item.label}</option>`).join("");
    select.value = dictionaries[current] ? current : normalizeLang(lang);
  }

  function translateKnown(lang) {
    const dict = dictionaries[normalizeLang(lang)] || dictionaries.ko;
    document.documentElement.lang = normalizeLang(lang);
    document.querySelectorAll("[data-i18n]").forEach((node) => {
      const key = node.getAttribute("data-i18n");
      if (key && Object.prototype.hasOwnProperty.call(dict, key)) node.textContent = dict[key];
    });
    document.querySelectorAll("[data-placeholder]").forEach((node) => {
      const key = node.getAttribute("data-placeholder");
      if (key && Object.prototype.hasOwnProperty.call(dict, key)) node.setAttribute("placeholder", dict[key]);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
      const key = node.getAttribute("data-i18n-placeholder");
      if (key && Object.prototype.hasOwnProperty.call(dict, key)) node.setAttribute("placeholder", dict[key]);
    });
    document.querySelectorAll("[data-i18n-title]").forEach((node) => {
      const key = node.getAttribute("data-i18n-title");
      if (key && Object.prototype.hasOwnProperty.call(dict, key)) node.setAttribute("title", dict[key]);
    });
    document.querySelectorAll("#langSelect, #languageSelect, select[data-lang-select], .lang-select").forEach((select) => installOptions(select, lang));
  }

  function callPageApply(lang) {
    const next = normalizeLang(lang);
    try { localStorage.setItem("lang", next); localStorage.setItem("siteLang", next); } catch (_) {}
    patchPageDictionaries();
    const calls = [
      () => typeof setLang === "function" && setLang(next),
      () => typeof applyLang === "function" && applyLang(next),
      () => typeof applyI18n === "function" && applyI18n(),
      () => typeof applyLanguage === "function" && applyLanguage(),
    ];
    for (const call of calls) {
      try {
        if (call()) break;
      } catch (_) {}
    }
    translateKnown(next);
  }

  function bindSelects(lang) {
    document.querySelectorAll("#langSelect, #languageSelect, select[data-lang-select], .lang-select").forEach((select) => {
      installOptions(select, lang);
      if (select.dataset.legendLangBound === "1") return;
      select.dataset.legendLangBound = "1";
      select.addEventListener("change", () => {
        const next = normalizeLang(select.value);
        try { localStorage.setItem("lang", next); localStorage.setItem("siteLang", next); } catch (_) {}
        setTimeout(() => callPageApply(next), 0);
      });
    });
  }

  function init() {
    const lang = selectedLang();
    patchPageDictionaries();
    bindSelects(lang);
    callPageApply(lang);
    setTimeout(() => {
      bindSelects(selectedLang());
      callPageApply(selectedLang());
    }, 80);
  }

